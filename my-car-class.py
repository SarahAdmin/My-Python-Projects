class car: 
  def __init__(self,brand,type,colour,noOfSeats): 
     self.brand = brand
     self.type = type 
     self.colour = colour 
     self.noOfSeats = noOfSeats 
def carFunct(self): 
  print('brand: '+self.brand)
  print('type: '+self.type) 
  print('colour: '+self.colour)
  print('noOfSeats: '+self.noOfSeats) 
par2 = car('Ford','Fiesta','Dark Blue','Five') 

